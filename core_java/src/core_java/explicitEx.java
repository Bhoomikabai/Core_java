package core_java;

public class explicitEx 
{
 public static void main(String[]args)
 {
	 double d = 500.4;
	 long l = (long)d;
	 System.out.println("value in long " + l);
	 int i = (int) d;
	 System.out.println("value in int " + i);
	 byte b = (byte) d;
	 System.out.println("value in byte " + b);
 }
}
