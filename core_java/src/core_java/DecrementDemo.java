package core_java;

public class DecrementDemo 
{
  public static void main(String[] args)
  {
	  //post decrement
	  int a = 10;
	      a--; //a=a-10
	      System.out.println(a);
	      
	      //pre decrement
	   int b =30;
	       --b; // b-30=b
	      System.out.println(b);
  }
}
