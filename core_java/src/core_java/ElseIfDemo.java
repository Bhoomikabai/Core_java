package core_java;

public class ElseIfDemo 
{
 public static void main(String[] args) 
 {
  int marks = 60;
  
  if(marks>90)
  {
	  System.out.println("A");
  }
  else if(marks > 75)
  {
	  System.out.println("B");
  }
  else if (marks > 60)
  {
	  System.out.println("C");
}
  else
  {
	  System.out.println("D");
  }
 }
}
